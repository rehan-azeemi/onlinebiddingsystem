package dto;

public class MessagesDTO {

	private String messageId;
	private String message;
	private String status;
	private String messageArrivalTime;
	private String sendToId;
	private String sendToName;
	private String sendById;
	private String sendByName;
	private String postedBidProducts;
	
	public String getMessageId() {
		return messageId;
	}
	public void setMessageId(String messageId) {
		this.messageId = messageId;
	}
	public String getMessage() {
		return message;
	}
	public void setMessage(String message) {
		this.message = message;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getMessageArrivalTime() {
		return messageArrivalTime;
	}
	public void setMessageArrivalTime(String messageArrivalTime) {
		this.messageArrivalTime = messageArrivalTime;
	}
	public String getSendToId() {
		return sendToId;
	}
	public void setSendToId(String sendToId) {
		this.sendToId = sendToId;
	}
	public String getSendToName() {
		return sendToName;
	}
	public void setSendToName(String sendToName) {
		this.sendToName = sendToName;
	}
	public String getSendById() {
		return sendById;
	}
	public void setSendById(String sendById) {
		this.sendById = sendById;
	}
	public String getSendByName() {
		return sendByName;
	}
	public void setSendByName(String sendByName) {
		this.sendByName = sendByName;
	}
	public String getPostedBidProducts() {
		return postedBidProducts;
	}
	public void setPostedBidProducts(String postedBidProducts) {
		this.postedBidProducts = postedBidProducts;
	}


	
}
